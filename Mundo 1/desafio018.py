import math
angulo = math.radians(float(input('Digite um valor inteiro como ângulo: ')))
seno = math.sin(angulo)
cosseno = math.cos(angulo)
tangente = math.tan(angulo)
print('O seno é {}.'.format(round(seno,4)))
print('O cosseno é {}.'.format(round(cosseno,4)))
print('A tangente é {}.'.format(round(tangente,4)))

